/**
 * 이 파일은 실행 중에 생긴 오류 값을 화면에서 읽을 수 있는 한 줄 문장으로 바꾸는 역할만 담당한다.
 * - 입력: 어떤 형태인지 확실하지 않은 오류 값을 받는다.
 * - 출력: 화면에 그대로 보여줄 수 있는 문자열을 돌려준다.
 * - 역할 분리: 서버 호출과 응답 해석은 다른 파일에서 담당하고, 이 파일은 오류 문구 변환만 담당한다.
 * - 실패 영향: 예상하지 못한 값이 들어와도 문자열로 바꿔 주기 때문에 오류 표시 단계가 중단되지 않는다.
 */
export const errorMessage = (err: unknown): string =>
  err instanceof Error ? err.message : String(err);
