/**
 * 서버에서 받은 회차 번호 배열을 화면의 회차 선택 목록으로 바꾸는 도구입니다.
 *
 * 하는 일
 * - 서버가 준 회차 배열 앞에 “다음 회차(첫 값 + 1)”를 붙여 줍니다.
 * - 값이 비어 있으면 빈 목록을 그대로 돌려줍니다.
 *
 * 무엇을 받고 무엇을 돌려주는지
 * - 받음: 정수 회차 배열
 * - 돌려줌: 드롭다운에 바로 넣을 수 있는 회차 배열
 *
 * 역할 나눔
 * - 서버 응답에서 정수 회차만 고르는 일은 `logic/parseDrawArr.ts`가 담당합니다.
 * - 실제 서버 호출은 `api/draw/drawNums.ts`가 담당합니다.
 *
 * 주의·화면에 미치는 영향
 * - 빈 배열이면 선택할 회차가 없어서 화면의 회차 선택이 비어 보일 수 있습니다.
 */
export const buildDrawList = (data: number[]): number[] => {
  if (data.length === 0) return [];
  return [data[0] + 1, ...data];
};
